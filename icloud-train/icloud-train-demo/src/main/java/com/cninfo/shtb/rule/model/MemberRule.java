package com.cninfo.shtb.rule.model;

public class MemberRule {
	private Age age;
	private Guanxi guanxi;
	private Money money;

	public Age getAge() {
		return age;
	}

	public void setAge(Age age) {
		this.age = age;
	}

	public Guanxi getGuanxi() {
		return guanxi;
	}

	public void setGuanxi(Guanxi guanxi) {
		this.guanxi = guanxi;
	}

	public Money getMoney() {
		return money;
	}

	public void setMoney(Money money) {
		this.money = money;
	}

}
