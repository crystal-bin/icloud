package com.icloud.stock.importer.handler;

public class XueqiuStockDetail {
	private String title;
	private String detailContent;
	private String companyInfoWork;
	private String totalMoney;
	private String totalStock;
	private String flowStock;

	private String stockCode;
	private String stockTitle;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDetailContent() {
		return detailContent;
	}

	public void setDetailContent(String detailContent) {
		this.detailContent = detailContent;
	}

	public String getCompanyInfoWork() {
		return companyInfoWork;
	}

	public void setCompanyInfoWork(String companyInfoWork) {
		this.companyInfoWork = companyInfoWork;
	}

	public String getTotalMoney() {
		return totalMoney;
	}

	public void setTotalMoney(String totalMoney) {
		this.totalMoney = totalMoney;
	}

	public String getTotalStock() {
		return totalStock;
	}

	public void setTotalStock(String totalStock) {
		this.totalStock = totalStock;
	}

	public String getFlowStock() {
		return flowStock;
	}

	public void setFlowStock(String flowStock) {
		this.flowStock = flowStock;
	}

	public String getStockCode() {
		return stockCode;
	}

	public void setStockCode(String stockCode) {
		this.stockCode = stockCode;
	}

	public String getStockTitle() {
		return stockTitle;
	}

	public void setStockTitle(String stockTitle) {
		this.stockTitle = stockTitle;
	}

}
